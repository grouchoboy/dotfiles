local function html_to_liveview()
  local line = vim.api.nvim_get_current_line()
  local new_line = line:gsub('<(%w+)', '<.%1'):gsub('</(%w+)>', '</.%1>')
  vim.api.nvim_set_current_line(new_line)
end

